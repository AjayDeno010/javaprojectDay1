package Module_1;

public class DataTypes {
    //DataTypes- Premetive,Arrays
    public static void main(String[] args) {
        //premitve data types
        //byte
        byte k=12;
        byte j=13;
        System.out.println("Byte"+" "+k+j);
        //short
        short s= 10;
        short s1= 10;
        System.out.println("Short"+" "+s+s1);
        //int
        int a=5;
        int b=6;
        System.out.println("int"+" "+a+b);
        //long
        long phone =9841175642l;
        System.out.println("Phone Number"+" "+phone);
        //float
        float milk= 23.30f;
        System.out.println("Float"+" "+milk);
        //double
        double d= 1234.0987;
        System.out.println("double"+" "+d);
        //Boolean
        Boolean Age = true;
        System.out.println("Boolean"+" "+Age);
        //char
        char c= 'A';
    }
}
